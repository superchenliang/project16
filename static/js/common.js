// top网站导航 移入事件
$(function(){
    $(".wzdhBox").css("display","none")
    $(".top_enter").mouseenter(function(){
        $(".wzdhBox").show();
    }).mouseleave(function(){
        $(".wzdhBox").hide();
    })

    $(".wzdhBox").mouseenter(function(){
        $(".wzdhBox").show();
    }).mouseleave(function(){
        $(".wzdhBox").hide();
    })

})


